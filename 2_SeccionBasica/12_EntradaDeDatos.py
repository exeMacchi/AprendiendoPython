# Pedirle el nombre al usuario.
nombre = input("Ingrese su nombre: ")
print(f"Nombre del usuario: {nombre}\n")

# Pedirle la edad al usuario, y convertir la cadena resultante en un número entero (int)
edad = int(input("Ingrese su edad: "))
print(f"Edad del usuario: {edad}\n")

# Pedirle la altura al usuario y convertir la cadena resultante en un número decimal (float)
altura = float(input("Ingrese su altura: "))
print(f"Altura del usuario: {altura}\n")