a = 4
b = 4

# Operadores relacionales o de comparación
igual_que = a == b
distinto_de = a != b

mayor_que = a > b
menor_que = a < b

mayor_igual_que = a >= b
menor_igual_que = a <= b

print(f"¿{a} es igual que {b}? --------> {igual_que}")
print(f"¿{a} es distinto de {b}? ------> {distinto_de}")
print(f"¿{a} es mayor que {b}? --------> {mayor_que}")
print(f"¿{a} es menor que {b}? --------> {menor_que}")
print(f"¿{a} es mayor igual que {b}? --> {mayor_igual_que}")
print(f"¿{a} es menor igual que {b}? --> {menor_igual_que}")