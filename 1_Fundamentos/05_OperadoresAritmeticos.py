# Suma y Resta de operandos (+ y -)
suma = 8 + 5
resta = 8 - 5

# Multiplicación y División (*, / y //)
multiplicacion = 8 * 5
division_decimal = 8 / 5 # Resultado float
division_entero = 8 // 5 # Resultado int (sin mantisa)

# Potenciación (base ** exponente)
potencia = 8 ** 5

# Resto (%)
resto = 8 % 5

print(f"Suma: {suma}") 
print(f"Resta: {resta}") 
print(f"Multiplicación: {multiplicacion}") 
print(f"Divisón normal: {division_decimal}") 
print(f"División baja: {division_entero}") 
print(f"Potencia: {potencia}") 
print(f"Resto: {resto}") 